# Los operadores logicos son utilizados para combinar expresiones booleanas.
print("Operadores logicos")
print("Operador and")
print("V y V =",True and True)  # True
print("V y F =", True and False) # False
print("F y V =", False and True) # False
print("F y F =", False and False) # False
print("Operador or")
print("V o V =",True or True)  # True
print("V o F =", True or False) # True
print("F o V =", False or True) # True
print("F o F =", False or False) # False
print("Operador not")
print("no V =", not True)  # False
print("no F =", not False) # True